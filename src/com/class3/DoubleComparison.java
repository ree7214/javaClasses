package com.class3;

public class DoubleComparison {

	public static void main(String[] args) {
		
		int dv1=40;
		int dv2=30;
		
		
		if (dv1>=dv2) {
			System.out.println("Double value 40 is larger than 30.");
		}else {
			System.out.println("Double value 30 is not larger than 40");
			
		}
		
		double num1=100;
		double num2=75;
		
		if(num1>num2) {
		System.out.println("Double value "+num1+" is larger than "+num2);
		
			
		}

	}

}
