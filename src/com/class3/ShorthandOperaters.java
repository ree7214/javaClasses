package com.class3;

public class ShorthandOperaters {
	public static void main(String[] args) {
		int num3 = 1000;
		num3 = 1000; // shorcut for printing syso+ctrl+space
		System.out.println();

		int num4 = 50;
		num4 += 20; // num4=num4+20
		System.out.println(num4);

		num4 -= 30; // num4=70-30
		System.out.println(num4);

		int short1 = 100;
		System.out.println(short1);

		int short2 = 100;
		short2 -= 67;
		System.out.println(short2);

		double cakePiece = 11;
		cakePiece /= 4;
		System.out.println(cakePiece);

		double cakePiece2 = 25;
		cakePiece2 %= 7;
		System.out.println(cakePiece2);

	}

}
