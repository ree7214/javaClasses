package ReplHomwork;

public class Repl76ArrayString {

	public static void main(String[] args) {
		/*Write a program that creates an array of strings 
		 * with the following values{"This", "is", "array", "of", "strings"} 
		 * and prints all values in one line. 
		 */
		
		String [] words= {"This", "is", "array","of","strings"};
		int size=words.length;
		for(int i=5; i<=size; i++) {
			System.out.println(words[0]+" "+words[1]+" "+words[2]+" "+words[3]+" "+words[4]);
		}
		
		
		
		
		
		
		

	}

}
